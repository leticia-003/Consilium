-- ==============================================================================
-- PASSO 2.3 - TABELA: PROCESS_TYPE_PHASE (RELACIONA TIPOS DE PROCESSO COM FASES)
-- ==============================================================================
CREATE TABLE LEGAL.PROCESS_TYPE_PHASE (
    PROCESS_TYPE_PHASE_ID SERIAL NOT NULL,
    PROCESS_PHASE_ID INT NOT NULL,
    PROCESS_TYPE_ID INT NOT NULL,
    PROCESS_TYPE_PHASE_ORDER SMALLINT NOT NULL,
    PROCESS_TYPE_PHASE_IS_OPTIONAL BOOLEAN NOT NULL DEFAULT FALSE,
    PROCESS_TYPE_PHASE_IS_ACTIVE BOOLEAN NOT NULL DEFAULT TRUE,

    -- PRIMARY KEY
    CONSTRAINT PK_PROCESS_TYPE_PHASE PRIMARY KEY (PROCESS_TYPE_PHASE_ID),

    -- FOREIGN KEYS
    CONSTRAINT FK_PROCESS_TYPE_PHASE_PROCESS_TYPE_01 FOREIGN KEY (PROCESS_TYPE_ID)
        REFERENCES PROCESS_TYPE(PROCESS_TYPE_ID),
    CONSTRAINT FK_PROCESS_TYPE_PHASE_PROCESS_PHASE_02 FOREIGN KEY (PROCESS_PHASE_ID)
        REFERENCES PROCESS_PHASE(PROCESS_PHASE_ID),

    -- UNIQUE KEYS
    CONSTRAINT UK_PROCESS_TYPE_PHASE_01 UNIQUE (PROCESS_TYPE_ID, PROCESS_PHASE_ID),
    CONSTRAINT UK_PROCESS_TYPE_PHASE_02 UNIQUE (PROCESS_TYPE_PHASE_ORDER, PROCESS_TYPE_ID)
);

-- ÍNDICES PARA AS FKS
CREATE INDEX IDX_PROCESS_TYPE_PHASE_01 ON PROCESS_TYPE_PHASE(PROCESS_TYPE_ID);
CREATE INDEX IDX_PROCESS_TYPE_PHASE_02 ON PROCESS_TYPE_PHASE(PROCESS_PHASE_ID);

