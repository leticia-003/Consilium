-- ==============================================================================
-- PASSO 2.5 - TABELA: PROCESS (PROCESSOS JURÍDICOS PRINCIPAIS)
-- ==============================================================================
CREATE TABLE LEGAL.PROCESS (
    PROCESS_ID UUID NOT NULL,
    PROCESS_NAME VARCHAR(255) NOT NULL,
    PROCESS_NUMBER VARCHAR(255) NOT NULL,
    CLIENT_ID UUID NOT NULL,
    LAWYER_ID UUID NOT NULL,
    PROCESS_ADVERSE_PART_NAME VARCHAR(255),
    PROCESS_OPPOSING_COUNSEL_NAME VARCHAR(255),
    PROCESS_CREATED_BY UUID NOT NULL,
    PROCESS_CREATED_AT TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP,
    PROCESS_CLOSED_AT TIMESTAMP,
	PROCESS_DSC TEXT,
	PROCESS_NEXT_HEARING_DATE TIMESTAMP,
    PROCESS_PRIORITY SMALLINT NOT NULL,
    PROCESS_COURT_INFO TEXT NOT NULL,
    PROCESS_TYPE_PHASE_ID SMALLINT NOT NULL,
    PROCESS_STATUS_ID SMALLINT NOT NULL,


    -- PRIMARY KEY
    CONSTRAINT PK_PROCESS PRIMARY KEY (PROCESS_ID),

    -- FOREIGN KEYS
    CONSTRAINT FK_PROCESS_CLIENT_01 FOREIGN KEY (CLIENT_ID)
        REFERENCES CORE.CLIENT(CLIENT_ID),
    CONSTRAINT FK_PROCESS_LAWYER_02 FOREIGN KEY (LAWYER_ID)
        REFERENCES CORE.LAWYER(LAWYER_ID),
    CONSTRAINT FK_PROCESS_ADMIN_03 FOREIGN KEY (PROCESS_CREATED_BY)
        REFERENCES CORE.ADMIN(ADMIN_ID),
    CONSTRAINT FK_PROCESS_TYPE_PHASE_04 FOREIGN KEY (PROCESS_TYPE_PHASE_ID)
        REFERENCES LEGAL.PROCESS_TYPE_PHASE(PROCESS_TYPE_PHASE_ID),
    CONSTRAINT FK_PROCESS_STATUS_05 FOREIGN KEY (PROCESS_STATUS_ID)
        REFERENCES LEGAL.PROCESS_STATUS(PROCESS_STATUS_ID),

    -- UNIQUE KEYS
    CONSTRAINT UK_PROCESS_01 UNIQUE (PROCESS_NUMBER, CLIENT_ID, LAWYER_ID)
);

-- ÍNDICES PARA AS FKS
CREATE INDEX IDX_PROCESS_01 ON LEGAL.PROCESS(PROCESS_STATUS_PHASE_ID);
CREATE INDEX IDX_PROCESS_02 ON LEGAL.PROCESS(CLIENT_ID);
CREATE INDEX IDX_PROCESS_03 ON LEGAL.PROCESS(LAWYER_ID);
CREATE INDEX IDX_PROCESS_04 ON LEGAL.PROCESS(PROCESS_CREATED_BY);
CREATE INDEX IDX_PROCESS_05 ON LEGAL.PROCESS(PROCESS_TYPE_PHASE_ID);
CREATE INDEX IDX_PROCESS_06 ON LEGAL.PROCESS(PROCESS_STATUS_ID);

